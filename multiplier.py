def addition(a,b):
    """Addition of two numbers!"""
    return a+b


# -------------------------------------

def multiplication(a,b):
    """Multiply with two numbers!"""
    return a*b


# ---------------------------------------

def substract(a,b):
    """Substract with two values"""
    return a-b


# ---------------------------------------

def mod(a):
    """Square of a number"""
    return a**a


